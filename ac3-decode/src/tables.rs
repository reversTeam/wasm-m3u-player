/// BAP 1: 3-level symmetric, 3 values per 5-bit group.
/// 3^3 = 27 valid entries + 5 padding = 32 entries.
/// Values are {-2/3, 0, 2/3}.
pub fn bap1_lookup(group: u32) -> [f32; 3] {
    const LEVELS: [f32; 3] = [-2.0 / 3.0, 0.0, 2.0 / 3.0];
    if group >= 27 {
        return [0.0, 0.0, 0.0];
    }
    let g = group as usize;
    [LEVELS[g / 9], LEVELS[(g % 9) / 3], LEVELS[g % 3]]
}

/// BAP 2: 5-level symmetric, 3 values per 7-bit group.
/// 5^3 = 125 valid entries + 3 padding = 128 entries.
/// Values are {-4/5, -2/5, 0, 2/5, 4/5}.
pub fn bap2_lookup(group: u32) -> [f32; 3] {
    const LEVELS: [f32; 5] = [-4.0 / 5.0, -2.0 / 5.0, 0.0, 2.0 / 5.0, 4.0 / 5.0];
    if group >= 125 {
        return [0.0, 0.0, 0.0];
    }
    let g = group as usize;
    [LEVELS[g / 25], LEVELS[(g % 25) / 5], LEVELS[g % 5]]
}

/// BAP 3: 7-level symmetric, 1 value per 3-bit code.
/// Values are {-6/7, -4/7, -2/7, 0, 2/7, 4/7, 6/7} + padding.
pub fn bap3_lookup(code: u32) -> f32 {
    const LEVELS: [f32; 8] = [
        -6.0 / 7.0,
        -4.0 / 7.0,
        -2.0 / 7.0,
        0.0,
        2.0 / 7.0,
        4.0 / 7.0,
        6.0 / 7.0,
        0.0,
    ];
    LEVELS[(code & 7) as usize]
}

/// BAP 4: 11-level symmetric, 2 values per 7-bit group.
/// 11^2 = 121 valid entries + 7 padding = 128 entries.
/// Values are {-10/11, -8/11, ..., 0, ..., 8/11, 10/11}.
pub fn bap4_lookup(group: u32) -> [f32; 2] {
    const LEVELS: [f32; 11] = [
        -10.0 / 11.0,
        -8.0 / 11.0,
        -6.0 / 11.0,
        -4.0 / 11.0,
        -2.0 / 11.0,
        0.0,
        2.0 / 11.0,
        4.0 / 11.0,
        6.0 / 11.0,
        8.0 / 11.0,
        10.0 / 11.0,
    ];
    if group >= 121 {
        return [0.0, 0.0];
    }
    let g = group as usize;
    [LEVELS[g / 11], LEVELS[g % 11]]
}

